var cardOne = document.querySelector(".card1");
cardOne.addEventListener("mousemove", function () {
    anime({
        targets: document.querySelector(".card1"),
        rotateY:"+=180",
        easing:'easeInOutSine',
        duration:400,
    });
})
cardOne.addEventListener("mouseleave", function () {
    anime({
        targets: document.querySelector(".card1"),
        rotateY:"+=180",
        easing:'easeInOutSine',
        duration:400,
    });
})
